def replace_symbols(text):
    if not text:
        return text

    text = re.sub(r'([ZzsSqwa¡`]+)Q', r'Q\1', text)

    for old, new in PAIRS:
        while old in text:
            text = text.replace(old, new)

    text = re.sub(r'([ेैुूं]+)्र', r'्र\1', text)
    text = re.sub(r'ं([ाेैुू]+)', r'\1ं', text)
    text = re.sub(r'([ \n])ा', r'\1श', text)

    text = text.replace('¯', 'f')
    text = text.replace('Ł', 'र्f')

    text = re.sub(r'([fŻ])([कखगघङचछजझञटठडड़ढढ़णतथदधनपफबभमयरलवशषसहक्ष])', r'\2\1', text)
    text = re.sub(r'([fŻ])(्)([कखगघङचछजझञटठडड़ढढ़णतथदधनपफबभमयरलवशषसहक्ष])', r'\2\3\1', text)
    text = re.sub(r'([fŻ])(्)([कखगघङचछजझञटठडड़ढढ़णतथदधनपफबभमयरलवशषसहक्ष])', r'\2\3\1', text)

    text = text.replace('f', 'ि')
    text = text.replace('Ż', 'िं')

    text = text.replace('±', 'Zं')
    text = re.sub(r'([कखगघचछजझटठडड़ढढ़णतथदधनपफबभमयरलळवशषसहक्षज्ञ])([ािीुूृेैोौंँ]*)([Z])', r'\3\1\2', text)
    text = re.sub(r'([कखगघचछजझटठडड़ढढ़णतथदधनपफबभमयरलळवशषसहक्षज्ञ])([्])([Z])', r'\3\1\2', text)
    text = text.replace('Z', 'र्')

    return text


def chanakya_to_unicode(text, chunk_size=6000):
    result = []
    for i in range(0, len(text), chunk_size):
        result.append(replace_symbols(text[i:i + chunk_size]))
    return ''.join(result)


